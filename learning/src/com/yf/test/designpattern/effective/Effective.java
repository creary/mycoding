package com.yf.test.designpattern.effective;

public class Effective {
	
	
	/**
	 * 把boolean一个基本类型的值转换为Boolean的引用                 
	 * @param b true或者false
	 * @return Boolean类型的值
	 */
	public static Boolean valueOf(boolean b){
		return b ? Boolean.TRUE :Boolean.FALSE;
	}


	
	
	
	
	
	

}
